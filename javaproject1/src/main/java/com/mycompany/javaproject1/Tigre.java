
package com.mycompany.javaproject1;

public class Tigre {
    private String nom;
    private String pays;
    private int age;

    public Tigre(String nom, String pays, int age) {
        this.nom = nom;
        this.pays = pays;
        this.age = age;
    }

    public Tigre() {
    }

    public String getNom() {
        return nom;
    }

    public String getPays() {
        return pays;
    }

    public int getAge() {
        return age;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public void setPays(String pays) {
        this.pays = pays;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
    
    
    
    
    
}
